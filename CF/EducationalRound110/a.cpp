    cerr << ")\n";

void solve(int t) {
    int a, b, c, d;
    scanf("%d%d%d%d", &a, &b, &c, &d);
    if ((a < c && b < c && a < d && b < d) || (d < b && d < a && c < b && c < a)) {
        printf("NO\n");
        return;
    }
    printf("YES\n");
}

int main() {
    int t;
    scanf("%d", &t);
    for (int i = 1; i <= t; i++) {
        solve(i);
    }
}
