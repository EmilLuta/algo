void solve(int t) {
    long long n;
    scanf("%lld", &n);
    while (n != 1 && n % 2 == 0) {
        n /= 2;
    }
    if (n == 1) {
        printf("NO\n");
    } else {
        printf("YES\n");
    }
}

int main() {
    int t;
    scanf("%d", &t);
    for (int i = 1; i <= t; i++) {
        solve(i);
    }
}
